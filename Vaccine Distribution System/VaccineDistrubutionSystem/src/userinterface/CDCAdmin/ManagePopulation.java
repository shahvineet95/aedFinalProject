    void populateTable(){
        DefaultTableModel model = (DefaultTableModel) populationTable.getModel();

        model.setRowCount(0);
        for (Network n : system.getNetworkList()) {
                    Object[] row = new Object[2];
                    row[0] = n;
                    row[1] = n.getPopulation();
                    model.addRow(row);
         }
    }
