    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) table_provider.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[3];
         for (WorkRequest wr : wq.getWorkRequestList()) {
            row[0] = wr.getVaccine();
            row[1] = wr.getHospital();
            row[2] = wr.getStatus();
            dtm.addRow(row);
        }
    }

