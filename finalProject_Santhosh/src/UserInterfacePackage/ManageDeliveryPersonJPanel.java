    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        // TODO add your handling code here:
        int selectedRow = table_deliveryPerson.getSelectedRow();
        if (selectedRow < 0) {
                JOptionPane.showMessageDialog(this, "Please select row to update details.");
            } else {
                User user = (User) table_deliveryPerson.getValueAt(selectedRow, 0);
                CardLayout layout = (CardLayout)viewJPanel.getLayout();
                viewJPanel.add(new UpdateDeliveryPersonJPanel(viewJPanel, user, directory));
                layout.next(viewJPanel);
            }
    }//GEN-LAST:event_button_updateActionPerformed


