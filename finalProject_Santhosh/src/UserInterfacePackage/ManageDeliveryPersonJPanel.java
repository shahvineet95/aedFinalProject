    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) table_deliveryPerson.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[4];
        for (User u : directory.getDirectory()) {
            row[0] = u;
            row[1] = u.getUserName();
            row[2] = u.getAddress();
            row[3] = u.getPhoneNumber();
            dtm.addRow(row);
        }
    }

