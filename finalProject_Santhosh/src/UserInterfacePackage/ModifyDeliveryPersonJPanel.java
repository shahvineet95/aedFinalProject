    private void toMainScreen() {
        CardLayout layout = (CardLayout)this.viewJPanel.getLayout();
        this.viewJPanel.remove(this);
        Component[] comps = this.viewJPanel.getComponents();
        for(Component comp : comps)
        {
            if(comp instanceof ViewDeliveryPerson)
            {
                ViewDeliveryPerson panel=(ViewDeliveryPerson)comp;
                panel.populateTable();
            }
        }
    }
    
