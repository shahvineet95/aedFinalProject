    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // TODO add your handling code here:
        String newName = txt_name.getText();
        String newUsername = txt_username.getText();
        String newPassword = txt_password.getText();
        String newAddress = txt_address.getText();
        String newPhone = txt_phoneNumber.getText();
        if (this.directory.updateUser(new User(newName, newUsername, newPassword, 
                newAddress, newPhone), user)) {
            JOptionPane.showMessageDialog(null, "Updation performed successfully!");
            
        } else {
            JOptionPane.showMessageDialog(null, "Updation failed!");
        }

        button_update.setEnabled(true);
        button_save.setEnabled(false);
    }//GEN-LAST:event_button_saveActionPerformed


