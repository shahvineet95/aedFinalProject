    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)table_deliveryPerson.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[5];
        for (User u : directory.getDirectory()) {
            row[0] = u;
            row[1] = u.getUserName();
            row[2] = u.getPassword();
            row[3] = u.getAddress();
            row[4] = u.getPhoneNumber();
            dtm.addRow(row);
        }
    }

